async def dork_gen(
    category: str = "",
    tech: str = "",
    vuln_type: str = "",
    target: str = "",
    limit: int = 50,
) -> dict:
    """Generate Google dorks for target discovery.

    Args:
        category:  Dork category — ``"vuln_sites"``, ``"config_leak"``,
                   ``"admin_panels"``, ``"db_dumps"``, ``"backup_files"``,
                   ``"git_exposed"``, ``"api_keys"``, ``"iot_devices"``,
                   ``"login_portals"``, ``"phpinfo"``, ``"error_messages"``,
                   ``"upload_forms"``, ``"exposed_cms"``, ``"shell_upload"``,
                   ``"deface_target"``, ``"s3_buckets"``, ``"exposed_docs"``.
        tech:      Technology-specific dorks — ``"wordpress"``, ``"laravel"``,
                   ``"django"``, ``"spring"``, ``"nodejs"``, ``"jenkins"``,
                   ``"gitlab"``, ``"kibana"``, ``"grafana"``.
        vuln_type: Vulnerability type — ``"sqli"``, ``"lfi"``, ``"xss"``, ``"rce"``.
        target:    Optional target domain to scope dorks (e.g. ``"example.com"``).
                   If provided, adds ``site:`` prefix to each dork.
        limit:     Max dorks to return per category (default 50).

    Returns:
        A dict with dorks list, categories used, and count.
    """
    result: dict[str, Any] = {
        "dorks": [],
        "sources": [],
        "count": 0,
        "error": None,
    }

    dorks: list[str] = []
    site_prefix = f"site:{target} " if target else ""

    if category:
        if category not in _CATEGORIES:
            result["error"] = (
                f"Unknown category: '{category}'. Available: {', '.join(sorted(_CATEGORIES))}"
            )
            return result
        cat_dorks = _CATEGORIES[category][:limit]
        dorks.extend(f"{site_prefix}{d}" for d in cat_dorks)
        result["sources"].append(f"category:{category}")

    if tech:
        if tech not in _TECH_DORKS:
            result["error"] = (
                f"Unknown tech: '{tech}'. Available: {', '.join(sorted(_TECH_DORKS))}"
            )
            return result
        tech_dorks = _TECH_DORKS[tech][:limit]
        dorks.extend(f"{site_prefix}{d}" for d in tech_dorks)
        result["sources"].append(f"tech:{tech}")

    if vuln_type:
        if vuln_type not in _VULN_DORKS:
            result["error"] = (
                f"Unknown vuln_type: '{vuln_type}'. Available: {', '.join(sorted(_VULN_DORKS))}"
            )
            return result
        vuln_dorks = _VULN_DORKS[vuln_type][:limit]
        dorks.extend(f"{site_prefix}{d}" for d in vuln_dorks)
        result["sources"].append(f"vuln:{vuln_type}")

    if not category and not tech and not vuln_type:
        result["error"] = "At least one of category, tech, or vuln_type must be provided."
        result["available_categories"] = sorted(_CATEGORIES.keys())
        result["available_tech"] = sorted(_TECH_DORKS.keys())
        result["available_vulns"] = sorted(_VULN_DORKS.keys())
        return result

    # Deduplicate while preserving order
    seen = set()
    unique = []
    for d in dorks:
        if d not in seen:
            seen.add(d)
            unique.append(d)

    result["dorks"] = [{"dork": d, "google_link": f"https://www.google.com/search?q={d.replace(' ', '+')}"} for d in unique]
    result["count"] = len(result["dorks"])
    return result
